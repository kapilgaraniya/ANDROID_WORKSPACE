package task_1/* 2. Write a program to make addition, Subtraction, Multiplication and Division of
Two Numbers. */

fun main()
{
    var a = 50
    var b = 25

    print("The addition is : ")
    println(a+b)
    print("The Subtraction is : ")
    println(a-b)
    print("The Multiplication is : ")
    println(a*b)
    print("The Division is : ")
    println(a/b)
    print("The modulo is : ")
    println(a%b)
}

